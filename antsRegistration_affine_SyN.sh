  rm -f ${tmpdir}/resample.h5 ${tmpdir}/clampmask.h5

  i=1
  while ((i < ${#_arg_resampled_output[@]})); do
    info "Generating final resampled output of ${_arg_moving[i - 1]} to $(basename ${_arg_resampled_output[i]})"
